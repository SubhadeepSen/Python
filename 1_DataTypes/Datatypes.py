# Python uses dynamic type,hence there is no need to specify the type of data

# Integer Datatype [Whole numbers, such as 3, 300, 465]
rollNo = 10;
print("Interger: ", rollNo);

# Float Datatype [Numbers with decimal point, such as 4.56, 100.00]
interest_rate = 8.85;
print("Float: ", interest_rate);

# String Datatype [Ordered sequence of characters]
# String is immutable
name = "Subhadeep Sen";
print("String: ", name);

# List Datatype [Ordered serquence of Objects]
numbers = [1,2,"John",4.45,"Rahul"];
print("List: ", numbers);

# Dictionary Datatype [Unordered key:value pairs]
orders = {"Tea":5, "Apples":12, "Bread": 4}
print("Dictionary: ", orders);

# Tuple Datatype [Ordered immutable serquence of Objects]
numbers = (1,2,3,4,5);
print("Tuple: ", numbers);

# Set Datatype [Unordered collection of unique objects]
numbers = {1,1,2,3,3,3,4,5,5,5};
print("Set: ", numbers);

# Boolean Datatype [Logical value such as True or False]
isValid = True;
print("Boolean: ", isValid);
