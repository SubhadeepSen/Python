#Comparision operator
isValid = (4 == 4);
print("(4 == 4) : ", isValid);

#Not Equal to operator
isValid = (4 != 5);
print("(4 != 5) : ", isValid);

#Greater than operator
isValid = (4 > 5);
print("(4 > 5) : ", isValid);

#Less than operator
isValid = (4 < 5);
print("(4 < 5) : ", isValid);

#Greater than equal to operator
isValid = (4 >= 4);
print("(4 >= 4) : ", isValid);

#Less than equal to operator
isValid = (4 <= 5);
print("(4 <= 5) : ", isValid);

#Logical Operators

#and
isValid = (4 < 5) and (8 > 5);
print("(4 < 5) and (8 > 5) : ", isValid);

#or
isValid = (4 > 5) or (8 < 5);
print("(4 > 5) or (8 < 5) : ", isValid);

#not
isValid = not(4 < 5);
print("not(4 < 5) : ", isValid);
